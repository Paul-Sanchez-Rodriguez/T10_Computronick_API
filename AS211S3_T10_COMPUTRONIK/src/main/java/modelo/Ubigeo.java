
package modelo;

public class Ubigeo {
    private String codigo;
    private String depubi;
    private String provubi;
    private String disubi;

    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public String getDepubi() {
        return depubi;
    }

    public void setDepubi(String depubi) {
        this.depubi = depubi;
    }

    public String getProvubi() {
        return provubi;
    }

    public void setProvubi(String provubi) {
        this.provubi = provubi;
    }

    public String getDisubi() {
        return disubi;
    }

    public void setDisubi(String disubi) {
        this.disubi = disubi;
    }
    
}
